package segitigacanggih;
public class SegitigaCanggih {

    public static void main(String[] args) {
        
        System.out.println("===== SOAL A =====");
        
        for (int c=5; c>=1; c--){
            for( int x=c; x<=5; x++){
                System.out.print(x+" ");
            }
            System.out.println("");
        }

        System.out.println("\n===== SOAL B =====");
        for(int w=1; w<=5; w++){
            
            for (int r=w; r<=5; r++){
                System.out.print(w+" ");
            }
            System.out.println("");
        }        
    }
    
}
